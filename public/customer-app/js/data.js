export function getGuideCategories() {
  return Object.entries(HEALTH_GUIDES).map(([key, data]) => ({
    id: key,
    title: data.title,
    icon: data.icon,
    articleCount: data.articles.length
  }));
}

export function getGuideArticles(categoryId) {
  return HEALTH_GUIDES[categoryId]?.articles || [];
}

export function getArticle(categoryId, articleId) {
  return HEALTH_GUIDES[categoryId]?.articles.find(a => a.id === articleId);
}

export function searchGuides(query) {
  const results = [];
  const lowerQuery = query.toLowerCase();
  
  Object.entries(HEALTH_GUIDES).forEach(([catKey, category]) => {
    category.articles.forEach(article => {
      if (article.title.toLowerCase().includes(lowerQuery) || 
          article.content.toLowerCase().includes(lowerQuery)) {
        results.push({
          categoryId: catKey,
          categoryTitle: category.title,
          ...article
        });
      }
    });
  });
  
  return results;
}


